#include "libft.h"

int				ft_str_is_numeric(char *str)
{
	int		i;

	if (!str)
		return (0);
	i = -1;
	if (str[0] == '-')
		++i;
	while (str[++i])
		if (!(str[i] >= '0' && str[i] <= '9'))
			return (0);
	return (1);
}
